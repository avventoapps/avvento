package org.avvento.apps.onlineradio;

public class Info {
    private String version;
    private String info;
    private String[] ticker;
    private String warning;

    public String getVersion() {
        return version;
    }

    public String getInfo() {
        return info;
    }

    public String[] getTicker() {
        return ticker;
    }

    public String getWarning() {
        return warning;
    }
}
